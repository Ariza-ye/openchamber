def escape_key(k):
    return "'" + k.replace("'", "\\'") + "'"

with codecs.open(file_path, 'r', 'utf-8') as f:
    content = f.read()

en_add = ""
zh_add = ""
for k, v in data.items():
    if f"{escape_key(k)}:" not in content:
        en_add += f"  {escape_key(k)}: {escape_key(k)},\n"
        zh_add += f"  {escape_key(k)}: {escape_key(v)},\n"

content = re.sub(r'(export const en: Record<string, string> = {)', r'\1\n' + en_add, content)
content = re.sub(r'(export const zhCN: Record<string, string> = {)', r'\1\n' + zh_add, content)

with codecs.open(file_path, 'w', 'utf-8') as f:
    f.write(content)
